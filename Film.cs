namespace LinqExercise;

public class Film
{


    public string Title { get; set; }
    public string Director { get; set; }
    
    public int ReleaseYear { get; set; }

    public double Rating { get; set; }

    public Film()
    {
        this.Title = Title;
        this.Director = Director;
        this.ReleaseYear = ReleaseYear;
        this.Rating = Rating;
    }









}



